package HotDrinkVendingMachine;

/**
 * Interfaccia che racchiude alcuni valori fissi.
 * Rappresentano la capienza della macchinetta, o le dosi utlizzate.
 */
public interface MaxValue {
    double SUGAR_DOSE = 0.25;
    double SUGAR_MAX = 1000.0;
    double MILK_MAX = 1000.0;
    int CUP_MAX = 500;
    int SPOON_MAX = 1000;
    double VODKA_MAX = 10.0;
}
